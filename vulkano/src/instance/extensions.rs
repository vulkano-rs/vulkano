pub use crate::extensions::{ExtensionRestriction, ExtensionRestrictionError, OneOfRequirements};
use crate::{OomError, Version};
use std::ffi::{CStr, CString};
use std::fmt::Formatter;

use super::VulkanLibrary;

// Generated by build.rs
include!(concat!(env!("OUT_DIR"), "/instance_extensions.rs"));

impl VulkanLibrary {
    pub fn instance_extensions(&self) -> Result<InstanceExtensions, OomError> {
        let properties: Vec<ash::vk::ExtensionProperties> =
            self.entry().enumerate_instance_extension_properties(None)?;

        Ok(InstanceExtensions::from(properties.iter().map(
            |property| unsafe { CStr::from_ptr(property.extension_name.as_ptr()) },
        )))
    }
}

